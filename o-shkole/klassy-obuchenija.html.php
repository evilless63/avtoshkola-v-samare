</ul>    
<p>Медицинская справка выдается сроком на 2 года.</p>
<p>При прохождении медицинской комиссии при себе иметь: паспорт, фото (3х4).</p>
<p>Для обучения необходимо приобрести комплект литературы:</p>
<ul>
    <li>- Правила дорожного движения 2015г.</li>
    <li>- Экзаменационные билеты А и В с комментариями 2015г.</li>
</ul>
<p align="center"><span style="text-decoration: underline;">Курс обучения. </span></p>
<p>Срок обучения на категорию «В» 2,5 месяца: 1 месяц – теория (2 раза в неделю по вечерам); 2 месяц – теория + практическое обучение на автомобиле; 0,5 месяца – сдача внутренних зачетов и экзамен в ГИБДД.</p>
<p>Обучение практическому вождению&nbsp; на автомобиле осуществляется по индивидуальному графику в течении рабочего дня.</p>
<p>Учебные автомобили: ВАЗ 21099, Калина, Киа Церато, Шевроле Авео, Шкода Октавиа</p>
<p>По окончанию срока обучения проводится сдача внутренних экзаменов:</p>
<ul>
    <li>- Теоретический (зачет – 3 билета, допускается 2 ошибки).</li>
    <li>- Практический (Требования МРЭО ГИБДД, проводится на закрытой площадке (автодроме) по адресу: г. Самара, ул. Свободы, 150.</li>
</ul>
<p>Выдача свидетельства об окончании курсов происходит по результатам сдачи внутренних экзаменов.</p>
